# Define a dictionary
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}

# Print key-value pairs of the dictionary
for key, value in my_dict.items():
    print(key, ":", value)
    
for value in my_dict.values():
    print(value)

for key in my_dict.keys():
    print(key)

